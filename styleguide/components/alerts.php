<h1>Alerts</h1>
<p>Alerts can be used for many things, but their main purpose is to gain the users attention</p>
<p>I used standard names for the alert classes like <code> alert--success, success--h, success--p, success--btn </code></p>

<!-- Success Alert -->
<div class="alert--success" role="alert">
    <h2 class="success--h">Success!</h2>
    <p class="success--p"> "Lorem ipsum dolor sit amet, consectetur adipiscing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua. Ut enim ad minim veniam,</p>
    <button class="success--btn">This is an alert box.</button>
</div>


<!-- Error Alert -->
<div class="alert--error">
    <h2 class="error--h">Error!</h2>
  <p class="error-p"> "Lorem ipsum dolor sit amet, consectetur adipiscing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua. Ut enim ad minim veniam,</p>
  <button class="error--btn">This is an alert box.</button>
</div>

<p>I also included a warning alert using class <code>alert-warning</code> </p>

<!-- Warning Alert -->
<div class="alert-warning">
    <strong>Warning!</strong>
    <button type="button" class="close" data-dismiss="alert">&times;</button>
</div>