            <h1>Utilities</h1>
            <p>
                The following utility classes are available to make minor changes on page layouts, colors,
                fonts, etc. If you find yourself using more than 5 utility classes you probably should be
                making a new component or module instead of using that many utilities.
            </p>
            <p>Utility classes are recognizable by the <code>.u-</code> that prefixes the class names</p>

            <hr>
            <h2>Colors</h2>
            <p>These classes may be used to change the color of text:</p>
            <ul>
                <li><code>.u-color--black</code></li>
                <li><code>.u-color--blue-light</code></li>
                <li><code>.u-color--error</code></li>
                <li><code>.u-color--grey-light</code></li>
                <li><code>.u-color--green</code></li>
                <li><code>.u-color--purple</code></li>
                <li><code>.u-color--red-dark</code></li>
                <li><code>.u-color--white</code></li>
            </ul>
            <hr>

            <h2>Background Colors</h2>
            <p>These classes may be used to change the background color of an element:</p>
            <ul>
                <li><code>.u-bg-color--black</code></li>
                <li><code>.u-bg-color--blue-light</code></li>
                <li><code>.u-bg-color--error</code></li>
                <li><code>.u-bg-color--grey-light</code></li>
                <li><code>.u-bg-color--green</code></li>
                <li><code>.u-bg-color--purple</code></li>
